CREATE PROCEDURE [dbo].[MAILING_TYPE_UPDATE]
	@NAME	NVARCHAR(50),
	@ID		SMALLINT
AS
BEGIN
	UPDATE
		Common.MailingType
	SET
		MailingTypeName = @NAME,
		MailingTypeLast = GETDATE()
	WHERE
		MailingTypeId = @ID
END
