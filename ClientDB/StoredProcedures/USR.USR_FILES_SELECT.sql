CREATE PROCEDURE [USR].[USR_FILES_SELECT]
	@LIST	VARCHAR(MAX)
AS
BEGIN
	SET NOCOUNT ON;

	SELECT a.UF_ID, a.UF_NAME, d.UF_DATA
	FROM 
		USR.USRFile a
		INNER JOIN dbo.USR.USRFileData d ON d.UF_ID = a.UF_ID
		INNER JOIN dbo.TableGUIDFromXML(@LIST) ON d.UF_ID = ID
END