USE [ClientDB]
	GO
	SET ANSI_NULLS ON
	GO
	SET QUOTED_IDENTIFIER ON
	GO
	CREATE PROCEDURE [dbo].[QUESTION_INSERT]	
	@NAME	VARCHAR(100),
	@DATE	SMALLDATETIME,
	@FREE	BIT,
	@ID	INT = NULL OUTPUT
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO dbo.QuestionTable(QuestionName, QuestionDate, QuestionFreeAnswer)
		VALUES(@NAME, @DATE, @FREE)
		
	SELECT @ID = SCOPE_IDENTITY()
END