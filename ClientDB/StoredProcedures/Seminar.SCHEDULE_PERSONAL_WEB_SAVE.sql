CREATE PROCEDURE [Seminar].[WEB_PERSONAL_INSERT]
	@SCHEDULE	UNIQUEIDENTIFIER,
	@CLIENT		INT,
	@PSEDO		NVARCHAR(256),
	@EMAIL		NVARCHAR(256)
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO Seminar.Personal(ID_SCHEDULE, ID_CLIENT, PSEDO, EMAIL, ID_STATUS)
		SELECT 
			@SCHEDULE, @CLIENT, @PSEDO, @EMAIL, 
			(
				SELECT ID 
				FROM Seminar.Status
				WHERE INDX = 1
			)
END
