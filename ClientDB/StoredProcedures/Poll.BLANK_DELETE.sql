USE [ClientDB]
GO
SET ANSI_NULLS ON
GO
SET QUOTED_IDENTIFIER ON
GO
CREATE PROCEDURE [Poll].[BLANK_DELETE]
	@ID	UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	DELETE 
	FROM Poll.Answer
	WHERE ID_QUESTION IN
		(
			SELECT ID
			FROM Poll.Question
			WHERE ID_BLANK = @ID
		)
		
	DELETE 
	FROM Poll.Question
	WHERE ID_BLANK = @ID
	
	DELETE
	FROM Poll.Blank	
	WHERE ID = @ID
END
