CREATE FUNCTION [dbo].[TableIDFromXML]
	(
		@SOURCE NVARCHAR(MAX)
	)
RETURNS @TBL TABLE
	(
		ID INT
	)
AS
BEGIN
	DECLARE @XML XML

	SET @XML = CAST(@SOURCE AS XML)

	INSERT INTO @TBL(ID)
		SELECT c.value('(.)', 'INT')
		FROM @xml.nodes('/LIST/ITEM') AS a(c)

	RETURN
END