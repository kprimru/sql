CREATE FUNCTION [Maintenance].[G_ProcedureLog]
()
RETURNS BIT
AS
BEGIN
	DECLARE @RES BIT

	SELECT @RES = PROCEDURE_LOG
	FROM Maintenance.Settings
	WHERE STATUS = 1

	RETURN @RES
END
GO
