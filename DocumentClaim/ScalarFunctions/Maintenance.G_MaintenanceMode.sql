CREATE FUNCTION [Maintenance].[G_MaintenanceMode]
()
RETURNS BIT
AS
BEGIN
	DECLARE @RES BIT

	SELECT @RES = MAINTENANCE_MODE
	FROM Maintenance.Settings
	WHERE STATUS = 1

	RETURN @RES
END
GO
