CREATE PROCEDURE [dbo].[ADDRESS_TEMPLATE_GET] 
	@atlid SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	SELECT 
		ATL_ID, ATL_CAPTION,
		ATL_INDEX, ATL_COUNTRY, ATL_REGION,	ATL_AREA, ATL_CITY_PREFIX, ATL_CITY,
		ATL_STR_PREFIX, ATL_STREET,	ATL_HOME, ATL_ACTIVE
	FROM dbo.AddressTemplateTable 
	WHERE ATL_ID = @atlid

	SET NOCOUNT OFF
END











