ALTER PROCEDURE [dbo].[CLIENT_DOCUMENT_SETTINGS_SELECT]
	@clientid INT
AS
BEGIN
	SET NOCOUNT ON;

	DECLARE
		@DebugError		VarChar(512),
		@DebugContext	Xml,
		@Params			Xml;

	EXEC [Debug].[Execution@Start]
		@Proc_Id		= @@ProcId,
		@Params			= @Params,
		@DebugContext	= @DebugContext OUT

	BEGIN TRY

		SELECT
			CDS_ID,
			CDS_ACT_CONTRACT, CDS_ACT_POS, CDS_ACT_POS_F, CDS_ACT_NAME, CDS_ACT_NAME_F,
			CDS_BILL_REST,
			CDS_INS_CONTRACT, CDS_INS_NAME
		FROM
			dbo.ClientDocumentSettingsTable
		WHERE CDS_ID_CLIENT = @clientid

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = NULL;
	END TRY
	BEGIN CATCH
		SET @DebugError = Error_Message();

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = @DebugError;

		EXEC [Maintenance].[ReRaise Error];
	END CATCH
END
GO
GRANT EXECUTE ON [dbo].[CLIENT_DOCUMENT_SETTINGS_SELECT] TO rl_client_r;
