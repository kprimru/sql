ALTER PROCEDURE [dbo].[BILL_FACT_ALL_GET]
AS
BEGIN
	SET NOCOUNT ON;

	DECLARE
		@DebugError		VarChar(512),
		@DebugContext	Xml,
		@Params			Xml;

	EXEC [Debug].[Execution@Start]
		@Proc_Id		= @@ProcId,
		@Params			= @Params,
		@DebugContext	= @DebugContext OUT

	BEGIN TRY

		SELECT BFM_DATE, COUNT(*) AS BFM_COUNT, BFM_ID_PERIOD, BILL_DATE
		FROM dbo.BillFactMasterTable
		GROUP BY BFM_DATE, BFM_ID_PERIOD, BILL_DATE
		ORDER BY BFM_DATE DESC

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = NULL;
	END TRY
	BEGIN CATCH
		SET @DebugError = Error_Message();

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = @DebugError;

		EXEC [Maintenance].[ReRaise Error];
	END CATCH
END
GO
GRANT EXECUTE ON [dbo].[BILL_FACT_ALL_GET] TO rl_bill_p;
GO
