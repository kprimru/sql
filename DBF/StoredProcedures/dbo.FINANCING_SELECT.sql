CREATE PROCEDURE [dbo].[FINANCING_SELECT] 
	@active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT FIN_ID, FIN_NAME 
	FROM dbo.FinancingTable 
	WHERE FIN_ACTIVE = ISNULL(@active, FIN_ACTIVE)
	ORDER BY FIN_NAME

	SET NOCOUNT OFF
END



