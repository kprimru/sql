CREATE PROCEDURE [dbo].[CONSIGNMENT_FACT_ALL_GET]	
AS
BEGIN
	SET NOCOUNT ON;

	SELECT CFM_FACT_DATE, COUNT(*) AS CFM_COUNT
	FROM dbo.ConsignmentFactMasterTable
	GROUP BY CFM_FACT_DATE
	ORDER BY CFM_FACT_DATE DESC
END


