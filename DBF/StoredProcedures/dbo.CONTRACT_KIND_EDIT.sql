CREATE PROCEDURE [dbo].[CONTRACT_KIND_EDIT]   
	@ID		SMALLINT,
	@NAME	VARCHAR(100),
	@HEADER VARCHAR(100),
	@CENTER	VARCHAR(100),
	@FOOTER	VARCHAR(100),
	@active BIT = 1
AS

BEGIN
	SET NOCOUNT ON
	
	UPDATE dbo.ContractKind
	SET CK_NAME	=	@NAME,
		CK_HEADER	=	@HEADER,
		CK_CENTER	=	@CENTER,
		CK_FOOTER	=	@FOOTER,
		CK_ACTIVE	=	@ACTIVE
	WHERE CK_ID = @ID

	SET NOCOUNT OFF
END

