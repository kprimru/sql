CREATE PROCEDURE [dbo].[PAY_COEF_SELECT]   
	@active BIT = NULL
AS

BEGIN
	SET NOCOUNT ON
	
	SELECT PC_START, PC_END, PC_VALUE, PC_ID 
	FROM dbo.PayCoefTable 
	WHERE PC_ACTIVE = ISNULL(@active, PC_ACTIVE)
	ORDER BY PC_START

	SET NOCOUNT OFF
END
