ALTER PROCEDURE [dbo].[SALE_OBJECT_SELECT]
	@active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON;

	DECLARE
		@DebugError		VarChar(512),
		@DebugContext	Xml,
		@Params			Xml;

	EXEC [Debug].[Execution@Start]
		@Proc_Id		= @@ProcId,
		@Params			= @Params,
		@DebugContext	= @DebugContext OUT

	BEGIN TRY

		SELECT SO_ID, SO_NAME, TX_ID, TX_NAME
		FROM
			dbo.SaleObjectTable a INNER JOIN
			dbo.TaxTable b ON a.SO_ID_TAX = b.TX_ID
		WHERE SO_ACTIVE = ISNULL(@active, SO_ACTIVE)

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = NULL;
	END TRY
	BEGIN CATCH
		SET @DebugError = Error_Message();

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = @DebugError;

		EXEC [Maintenance].[ReRaise Error];
	END CATCH
END
GO
GRANT EXECUTE ON [dbo].[SALE_OBJECT_SELECT] TO rl_sale_object_r;
GO
