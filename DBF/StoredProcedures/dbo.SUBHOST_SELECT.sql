CREATE PROCEDURE [dbo].[SUBHOST_SELECT] 
    @active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT SH_ID, SH_FULL_NAME, SH_SHORT_NAME, SH_LST_NAME, SH_ORDER, SH_ACTIVE 
	FROM dbo.SubhostTable 
	WHERE SH_ACTIVE = ISNULL(@active, SH_ACTIVE)
	ORDER BY SH_ORDER

	SET NOCOUNT OFF
END










