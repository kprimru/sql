ALTER PROCEDURE [dbo].[CATEGORY_GET]
	@ID	INT
AS
BEGIN
	SET NOCOUNT ON;

	SELECT ID_MASTER, NAME
	FROM dbo.Category
	WHERE ID = @ID
END
