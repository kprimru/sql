ALTER PROCEDURE [dbo].[USER_DELETE]
	@ID	INT
AS
BEGIN
	SET NOCOUNT ON;

	DELETE
	FROM dbo.Users
	WHERE ID = @ID
END
GO
GRANT EXECUTE ON [dbo].[USER_DELETE] TO rl_admin;
GO
