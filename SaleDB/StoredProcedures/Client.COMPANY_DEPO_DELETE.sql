CREATE PROCEDURE [Client].[COMPANY_DEPO_DELETE]
	@COMPANY_NUMBER		INT
AS
BEGIN
	SET NOCOUNT ON;

    UPDATE 
		Client.DepoNumbers
	SET
		STATUS = 1,
		COMPANY_NAME = NULL,
		COMPANY_NUMBER = NULL
	WHERE
		COMPANY_NUMBER = @COMPANY_NUMBER
END
