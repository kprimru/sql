IF OBJECT_ID('[dbo].[DateOf]', 'FN') IS NULL EXEC('CREATE FUNCTION [dbo].[DateOf] () RETURNS Int AS BEGIN RETURN NULL END')
GO
CREATE FUNCTION [dbo].[DateOf]
(
	@DT	DATETIME
)
RETURNS DATETIME
WITH SCHEMABINDING
AS
BEGIN
	RETURN CONVERT(DATETIME, CONVERT(VARCHAR(20), @DT, 112), 112)
END
GO
