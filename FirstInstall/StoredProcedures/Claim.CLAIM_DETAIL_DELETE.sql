ALTER PROCEDURE [Claim].[CLAIM_DETAIL_DELETE]
	@ID	UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	DELETE
	FROM Claim.ClaimDetail
	WHERE CLD_ID IN
		(
			SELECT *
			FROM Common.TableFromList(@ID, ',')
		)
END
GO
GRANT EXECUTE ON [Claim].[CLAIM_DETAIL_DELETE] TO rl_claim_w;
GO
