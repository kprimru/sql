CREATE PROCEDURE [Distr].[DISTR_NEW_INSERT]
	@TYPE	UNIQUEIDENTIFIER,
	@NUM	INT,
	@COMP	TINYINT,
	@SYS	UNIQUEIDENTIFIER,
	@NET	UNIQUEIDENTIFIER,
	@DATE	SMALLDATETIME
AS
BEGIN
	SET NOCOUNT ON;

	INSERT INTO Distr.DistrIncome(ID_TYPE, NUM, COMP, ID_SYSTEM, ID_NET, INCOME_DATE)
		VALUES(@TYPE, @NUM, @COMP, @SYS, @NET, @DATE)
END
