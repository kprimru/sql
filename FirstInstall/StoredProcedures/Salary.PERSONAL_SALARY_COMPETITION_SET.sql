CREATE PROCEDURE [Salary].[PERSONAL_SALARY_COMPETITION_SET]
	@PS_ID			UNIQUEIDENTIFIER,
	@COMPETITION	UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	UPDATE Salary.PersonalSalary
	SET PS_ID_COMPETITION = @COMPETITION
	WHERE PS_ID = @PS_ID
END
