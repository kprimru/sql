ALTER PROCEDURE [Security].[USER_LAST]
	@DT	DATETIME = NULL OUTPUT
AS
BEGIN
	SET NOCOUNT ON;

	SELECT	@DT = MAX(USMS_LAST)
	FROM	Security.Users
END
GO
