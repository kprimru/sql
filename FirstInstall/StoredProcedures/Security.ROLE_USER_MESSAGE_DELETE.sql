ALTER PROCEDURE [Security].[ROLE_USER_MESSAGE_DELETE]
	@US_ID	UNIQUEIDENTIFIER,
	@RL_ID	UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	DELETE
	FROM Security.RoleMessages
	WHERE RM_ID_ROLE = @RL_ID AND RM_ID_USER = @US_ID
END
GO
