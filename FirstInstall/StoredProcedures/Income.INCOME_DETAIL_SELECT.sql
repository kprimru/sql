ALTER PROCEDURE [Income].[INCOME_DETAIL_SELECT]
	@IN_ID UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	SELECT
		ID_ID, IN_ID,
		SYS_ID, SYS_ID_MASTER, SYS_SHORT,
		DT_ID, DT_ID_MASTER, DT_NAME,
		NT_ID, NT_ID_MASTER, NT_NAME,
		TT_ID, TT_ID_MASTER, TT_NAME,
		ID_COUNT, ID_DEL_SUM, ID_DEL_PRICE, ID_DEL_DISCOUNT,
		ID_ACTION, ID_RESTORE, ID_EXCHANGE, 
		ID_FULL_DATE, ID_SALARY,
		PR_FIRST_ID, PR_FIRST_ID_MASTER, PR_FIRST_NAME,
		PR_FULL_ID, PR_FULL_ID_MASTER, PR_FULL_NAME,
		ID_MON_CNT,
		ID_SUP_PRICE, ID_SUP_DISCOUNT, ID_SUP_MONTH, 
		ID_LOCK, ID_REPAY, ID_NOTE, ID_INSTALL
	FROM Income.IncomeDetailFullView
	WHERE IN_ID = @IN_ID
END
GO
GRANT EXECUTE ON [Income].[INCOME_DETAIL_SELECT] TO rl_income_r;
