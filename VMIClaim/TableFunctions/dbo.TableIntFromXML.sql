ALTER FUNCTION [dbo].[TableIntFromXML]
	(
		@SOURCE NVARCHAR(MAX)
	)
RETURNS @TBL TABLE
	(
		ID INT
	)
AS
BEGIN
	DECLARE @XML XML

	SET @XML = CAST(@SOURCE AS XML)

	INSERT INTO @TBL(ID)
		SELECT c.value('(@id)', 'INT')
		FROM @xml.nodes('/root/item') AS a(c)

	RETURN
END
GO
