ALTER PROCEDURE [dbo].[AREA_GET]
  @areaid SMALLINT = NULL
AS

BEGIN
	SET NOCOUNT ON

	SELECT AR_ID, AR_NAME, AR_ACTIVE
	FROM dbo.AreaTable
	WHERE AR_ID = @areaid

	SET NOCOUNT OFF
END






GO
GRANT EXECUTE ON [dbo].[AREA_GET] TO rl_area_r;
GO