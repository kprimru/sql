ALTER PROCEDURE [dbo].[COURIER_TYPE_DELETE]
	@id SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.CourierTypeTable
	WHERE COT_ID = @id

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[COURIER_TYPE_DELETE] TO rl_courier_type_d;
