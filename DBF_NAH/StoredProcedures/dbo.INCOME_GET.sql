ALTER PROCEDURE [dbo].[INCOME_GET]
	@inid INT

AS
BEGIN
	SET NOCOUNT ON;

	SELECT IN_DATE, IN_SUM, IN_PAY_DATE, IN_PAY_NUM, IN_PRIMARY
	FROM dbo.IncomeTable
	WHERE IN_ID = @inid
END


GO
GRANT EXECUTE ON [dbo].[INCOME_GET] TO rl_income_r;
GO