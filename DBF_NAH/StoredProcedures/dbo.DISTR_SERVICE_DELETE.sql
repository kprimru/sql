ALTER PROCEDURE [dbo].[DISTR_SERVICE_DELETE]
	@dsid SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.SystemServiceTable
	WHERE DSS_ID = @dsid

	SET NOCOUNT OFF
END



GO
GRANT EXECUTE ON [dbo].[DISTR_SERVICE_DELETE] TO rl_distr_service_d;
GO