ALTER PROCEDURE [dbo].[SYSTEM_DELETE]
	@systemid SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.SystemTable
	WHERE SYS_ID = @systemid

	SET NOCOUNT OFF
END



GO
GRANT EXECUTE ON [dbo].[SYSTEM_DELETE] TO rl_system_d;
GO