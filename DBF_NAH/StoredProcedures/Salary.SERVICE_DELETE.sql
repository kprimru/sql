ALTER PROCEDURE [Salary].[SERVICE_DELETE]
	@ID	UNIQUEIDENTIFIER
AS
BEGIN
	SET NOCOUNT ON;

	DELETE FROM Salary.ServiceDetail WHERE ID_SALARY = @ID
	DELETE FROM Salary.Service WHERE ID = @ID
END

GO
GRANT EXECUTE ON [Salary].[SERVICE_DELETE] TO rl_courier_pay;
