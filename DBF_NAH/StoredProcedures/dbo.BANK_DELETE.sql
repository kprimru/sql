ALTER PROCEDURE [dbo].[BANK_DELETE]
	@bankid INT
AS
BEGIN
	SET NOCOUNT ON

	DELETE FROM dbo.BankTable WHERE BA_ID = @bankid

	SET NOCOUNT OFF
END
GO
GRANT EXECUTE ON [dbo].[BANK_DELETE] TO rl_bank_d;
GO