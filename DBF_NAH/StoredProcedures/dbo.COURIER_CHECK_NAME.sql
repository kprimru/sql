ALTER PROCEDURE [dbo].[COURIER_CHECK_NAME]
	@couriername VARCHAR(100)
AS
BEGIN
	SET NOCOUNT ON

	SELECT COUR_ID
	FROM dbo.CourierTable
	WHERE COUR_NAME = @couriername

	SET NOCOUNT OFF
END





GO
GRANT EXECUTE ON [dbo].[COURIER_CHECK_NAME] TO rl_courier_w;
GO