ALTER PROCEDURE [dbo].[SYSTEM_CHECK_NAME]
	@systemname VARCHAR(100)
AS
BEGIN
	SET NOCOUNT ON

	SELECT SYS_ID
	FROM dbo.SystemTable
	WHERE SYS_NAME = @systemname

	SET NOCOUNT OFF
END







GO
GRANT EXECUTE ON [dbo].[SYSTEM_CHECK_NAME] TO rl_system_w;
GO