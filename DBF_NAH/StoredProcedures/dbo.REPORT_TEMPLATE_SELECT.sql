ALTER PROCEDURE [dbo].[REPORT_TEMPLATE_SELECT]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT RT_ID, RT_NAME		--, RT_TEXT
	FROM dbo.ReportTemplateTable
	ORDER BY RT_NAME

	SET NOCOUNT OFF
END






GO
GRANT EXECUTE ON [dbo].[REPORT_TEMPLATE_SELECT] TO rl_report_r;
GO