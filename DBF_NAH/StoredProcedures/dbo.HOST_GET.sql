ALTER PROCEDURE [dbo].[HOST_GET]
	@hostid SMALLINT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT HST_ID, HST_NAME, HST_REG_NAME, HST_ACTIVE
	FROM dbo.HostTable
	WHERE HST_ID = @hostid

	SET NOCOUNT OFF
END









GO
GRANT EXECUTE ON [dbo].[HOST_GET] TO rl_host_r;
GO