ALTER PROCEDURE [dbo].[COURIER_TYPE_GET]
	@id SMALLINT
AS

BEGIN
	SET NOCOUNT ON

	SELECT COT_NAME, COT_ID, COT_ACTIVE
	FROM dbo.CourierTypeTable
	WHERE COT_ID = @id

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[COURIER_TYPE_GET] TO rl_courier_type_r;
GO