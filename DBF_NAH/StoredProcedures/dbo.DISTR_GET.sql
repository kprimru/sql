ALTER PROCEDURE [dbo].[DISTR_GET]
	@distrid INT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT DIS_NUM, DIS_COMP_NUM, SYS_ID, SYS_SHORT_NAME, DIS_ACTIVE, DIS_STR
	FROM dbo.DistrView WITH(NOEXPAND)
	WHERE DIS_ID = @distrid

	SET NOCOUNT OFF
END
GO
GRANT EXECUTE ON [dbo].[DISTR_GET] TO rl_distr_r;
GO