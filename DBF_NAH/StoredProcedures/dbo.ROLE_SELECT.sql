ALTER PROCEDURE [dbo].[ROLE_SELECT]
	@active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT ROLE_ID, ROLE_NAME, ROLE_NOTE
	FROM dbo.RoleTable
	ORDER BY ROLE_NAME

	SET NOCOUNT OFF
END




GO
GRANT EXECUTE ON [dbo].[ROLE_SELECT] TO rl_role_r;
GO