ALTER PROCEDURE [dbo].[PAY_COEF_GET]
	@id SMALLINT
AS

BEGIN
	SET NOCOUNT ON

	SELECT PC_START, PC_END, PC_VALUE, PC_ID, PC_ACTIVE
	FROM dbo.PayCoefTable
	WHERE PC_ID = @id

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[PAY_COEF_GET] TO rl_pay_coef_r;
GO