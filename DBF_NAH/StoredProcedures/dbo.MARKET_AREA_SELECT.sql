ALTER PROCEDURE [dbo].[MARKET_AREA_SELECT]
	@active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT MA_ID, MA_NAME, MA_SHORT_NAME
	FROM dbo.MarketAreaTable
	WHERE MA_ACTIVE = ISNULL(@active, MA_ACTIVE)
	ORDER BY MA_NAME

	SET NOCOUNT OFF
END





GO
GRANT EXECUTE ON [dbo].[MARKET_AREA_SELECT] TO rl_market_area_r;
GO