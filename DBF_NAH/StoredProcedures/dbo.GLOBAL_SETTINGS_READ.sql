ALTER PROCEDURE [dbo].[GLOBAL_SETTINGS_READ]
	@NAME	VARCHAR(50)
AS
BEGIN
	SET NOCOUNT ON;

	SELECT GS_VALUE
	FROM dbo.GlobalSettingsTable
	WHERE GS_NAME = @NAME
END

GO
GRANT EXECUTE ON [dbo].[GLOBAL_SETTINGS_READ] TO rl_global_settings_r;
GO