ALTER PROCEDURE [dbo].[PRICE_GOOD_SELECT]
	@active BIT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT PGD_ID, PGD_NAME
	FROM 
		dbo.PriceGoodTable
	WHERE PGD_ACTIVE = ISNULL(@active, PGD_ACTIVE)
	ORDER BY PGD_NAME

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[PRICE_GOOD_SELECT] TO rl_price_good_r;
GO