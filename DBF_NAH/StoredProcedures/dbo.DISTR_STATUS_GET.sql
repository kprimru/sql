ALTER PROCEDURE [dbo].[DISTR_STATUS_GET]
	@dsid SMALLINT = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT DS_NAME, DS_REG, DS_ACTIVE
	FROM dbo.DistrStatusTable
	WHERE DS_ID = @dsid

	SET	NOCOUNT OFF
END













GO
GRANT EXECUTE ON [dbo].[DISTR_STATUS_GET] TO rl_distr_status_r;
GO