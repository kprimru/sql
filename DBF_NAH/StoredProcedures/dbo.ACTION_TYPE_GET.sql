ALTER PROCEDURE [dbo].[ACTION_TYPE_GET]
	@ID SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	SELECT ACTT_ID, ACTT_NAME, ACTT_ACTIVE
	FROM dbo.ActionType
	WHERE ACTT_ID = @ID

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[ACTION_TYPE_GET] TO rl_action_type_r;
GO