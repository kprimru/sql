ALTER PROCEDURE [dbo].[COURIER_DELETE]
	@courierid SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.CourierTable
	WHERE COUR_ID = @courierid

	SET NOCOUNT OFF
END
GO
GRANT EXECUTE ON [dbo].[COURIER_DELETE] TO rl_courier_d;
GO