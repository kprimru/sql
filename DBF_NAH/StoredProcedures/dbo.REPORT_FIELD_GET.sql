ALTER PROCEDURE [dbo].[REPORT_FIELD_GET]
AS
BEGIN
	SET NOCOUNT ON;

	SELECT RF_ID, RF_NAME, RF_CAPTION, RF_ORDER
	FROM dbo.ReportFieldTable
	ORDER BY RF_ORDER

	SET NOCOUNT OFF
END





GO
GRANT EXECUTE ON [dbo].[REPORT_FIELD_GET] TO rl_all_r;
GO