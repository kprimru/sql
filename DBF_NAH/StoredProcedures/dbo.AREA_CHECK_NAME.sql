ALTER PROCEDURE [dbo].[AREA_CHECK_NAME]
	@areaname VARCHAR(100)
AS
BEGIN
	SET NOCOUNT ON

	SELECT AR_ID
	FROM dbo.AreaTable
	WHERE AR_NAME = @areaname

	SET NOCOUNT OFF
END
GO
GRANT EXECUTE ON [dbo].[AREA_CHECK_NAME] TO rl_area_w;
GO