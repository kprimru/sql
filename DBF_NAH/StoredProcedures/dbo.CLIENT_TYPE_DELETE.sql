ALTER PROCEDURE [dbo].[CLIENT_TYPE_DELETE]
	@id SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.ClientTypeTable
	WHERE CLT_ID = @id

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[CLIENT_TYPE_DELETE] TO rl_client_type_d;
GO