ALTER PROCEDURE [dbo].[REG_NODE_MOVE_CHECK]
	@periodid SMALLINT
AS
BEGIN
	SET NOCOUNT ON;

	IF EXISTS
		(
			SELECT *
			FROM dbo.PeriodRegTable
			WHERE REG_ID_PERIOD = @periodid
		)
		SELECT 1 AS RES
	ELSE
		SELECT 0 AS RES
END

GO
GRANT EXECUTE ON [dbo].[REG_NODE_MOVE_CHECK] TO rl_reg_node_history_w;
