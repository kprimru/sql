ALTER PROCEDURE [dbo].[CLIENT_BILL_FACT_ROW_DELETE]
	@ROWLIST VARCHAR(MAX)
AS
BEGIN
	SET NOCOUNT ON;

	DECLARE
		@DebugError		VarChar(512),
		@DebugContext	Xml,
		@Params			Xml;

	EXEC [Debug].[Execution@Start]
		@Proc_Id		= @@ProcId,
		@Params			= @Params,
		@DebugContext	= @DebugContext OUT

	BEGIN TRY

		DECLARE @list TABLE (ROW_ID INT)

		INSERT INTO @list
			SELECT *
			FROM dbo.GET_TABLE_FROM_LIST(@ROWLIST, ',')

		DELETE
		FROM dbo.BillFactDetailTable
		WHERE BFD_ID IN
			(
				SELECT ROW_ID FROM @list
			)

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = NULL;
	END TRY
	BEGIN CATCH
		SET @DebugError = Error_Message();

		EXEC [Debug].[Execution@Finish] @DebugContext = @DebugContext, @Error = @DebugError;

		EXEC [Maintenance].[ReRaise Error];
	END CATCH
END

GO
GRANT EXECUTE ON [dbo].[CLIENT_BILL_FACT_ROW_DELETE] TO rl_bill_w;
