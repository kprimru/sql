ALTER PROCEDURE [dbo].[REPORT_TYPE_GET]
  @rtid int = NULL
AS
BEGIN
	SET NOCOUNT ON

	SELECT RTY_ID, RTY_NAME
	FROM dbo.ReportTypeTable
	WHERE RTY_ID = @rtid

	SET NOCOUNT OFF
END


GO
GRANT EXECUTE ON [dbo].[REPORT_TYPE_GET] TO rl_report_type_r;
GO