ALTER PROCEDURE [dbo].[ACTION_TRY_DELETE]
	@ID SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DECLARE @res INT
	DECLARE @txt VARCHAR(MAX)

	SET @res = 0
	SET @txt = ''


	SELECT @res AS RES, @txt AS TXT

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[ACTION_TRY_DELETE] TO rl_action_d;
GO