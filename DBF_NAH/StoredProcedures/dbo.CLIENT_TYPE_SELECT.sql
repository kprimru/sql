ALTER PROCEDURE [dbo].[CLIENT_TYPE_SELECT]
	@active BIT = NULL
AS

BEGIN
	SET NOCOUNT ON

	SELECT CLT_NAME, CLT_ID
	FROM dbo.ClientTypeTable
	WHERE CLT_ACTIVE = ISNULL(@active, CLT_ACTIVE)
	ORDER BY CLT_NAME

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[CLIENT_TYPE_SELECT] TO rl_client_type_r;
GO