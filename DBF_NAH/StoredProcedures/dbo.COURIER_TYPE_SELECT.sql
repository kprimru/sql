ALTER PROCEDURE [dbo].[COURIER_TYPE_SELECT]
	@active BIT = NULL
AS

BEGIN
	SET NOCOUNT ON

	SELECT COT_NAME, COT_ID
	FROM dbo.CourierTypeTable
	WHERE COT_ACTIVE = ISNULL(@active, COT_ACTIVE)
	ORDER BY COT_NAME

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[COURIER_TYPE_SELECT] TO rl_courier_type_r;
GO