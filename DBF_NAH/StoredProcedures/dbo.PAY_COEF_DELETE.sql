ALTER PROCEDURE [dbo].[PAY_COEF_DELETE]
	@id SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE
	FROM dbo.PayCoefTable
	WHERE PC_ID = @id

	SET NOCOUNT OFF
END

GO
GRANT EXECUTE ON [dbo].[PAY_COEF_DELETE] TO rl_pay_coef_d;
GO