ALTER PROCEDURE [dbo].[COUNTRY_DELETE]
	@countryid SMALLINT
AS
BEGIN
	SET NOCOUNT ON

	DELETE FROM dbo.CountryTable WHERE CNT_ID = @countryid

	SET NOCOUNT OFF
END
GO
GRANT EXECUTE ON [dbo].[COUNTRY_DELETE] TO rl_country_d;
GO